"""Ветвления. Проверка на истину True или ложь False. Блоки кода. Математика"""

x = int(input("Введите целое число: "))
if x > 0:
    print(f"Число {x} положительное")
    squared = x ** 2
    print(f"Число {x} в квадрате равно {squared}")
elif x < 0:
    print(f"Число {x // 2} отрицательное") # // - деление на цело
else:
    print(f"Число {x} является нулём")

print("Эта строка выводится всегда, так как не имеет пробелов в начале.")