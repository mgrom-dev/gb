    /**
     * –†–µ–∞–ª–∏–∑—É–π—Ç–µ –º–µ—Ç–æ–¥, –ø—Ä–∏–Ω–∏–º–∞—é—â–∏–π –≤ –∫–∞—á–µ—Å—Ç–≤–µ –∞—Ä–≥—É–º–µ–Ω—Ç–∞ —Ü–µ–ª–æ—á–∏—Å–ª–µ–Ω–Ω—ã–π –º–∞—Å—Å–∏–≤. –ï—Å–ª–∏
     * –¥–ª–∏–Ω–∞ –º–∞—Å—Å–∏–≤–∞ –º–µ–Ω—å—à–µ –Ω–µ–∫–æ—Ç–æ—Ä–æ–≥–æ –∑–∞–¥–∞–Ω–Ω–æ–≥–æ –º–∏–Ω–∏–º—É–º–∞, –º–µ—Ç–æ–¥ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç -1, –≤
     * –∫–∞—á–µ—Å—Ç–≤–µ –∫–æ–¥–∞ –æ—à–∏–±–∫–∏, –∏–Ω–∞—á–µ - –¥–ª–∏–Ω—É –º–∞—Å—Å–∏–≤–∞.
     */
    static int getArr(int array[]) {
        if (array.length < MIN)
            return -1;
        return array.length;
    }

    /**
     * üìî –†–µ–∞–ª–∏–∑—É–π—Ç–µ –º–µ—Ç–æ–¥, –ø—Ä–∏–Ω–∏–º–∞—é—â–∏–π –≤ –∫–∞—á–µ—Å—Ç–≤–µ –∞—Ä–≥—É–º–µ–Ω—Ç–∞ —Ü–µ–ª–æ—á–∏—Å–ª–µ–Ω–Ω—ã–π –º–∞—Å—Å–∏–≤ –∏
     * –Ω–µ–∫–æ—Ç–æ—Ä–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ. –ú–µ—Ç–æ–¥ –∏—â–µ—Ç –≤ –º–∞—Å—Å–∏–≤–µ –∑–∞–¥–∞–Ω–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –µ–≥–æ
     * –∏–Ω–¥–µ–∫—Å. –ü—Ä–∏ —ç—Ç–æ–º, –Ω–∞–ø—Ä–∏–º–µ—Ä:
     * 
     * 1. –µ—Å–ª–∏ –¥–ª–∏–Ω–∞ –º–∞—Å—Å–∏–≤–∞ –º–µ–Ω—å—à–µ –Ω–µ–∫–æ—Ç–æ—Ä–æ–≥–æ –∑–∞–¥–∞–Ω–Ω–æ–≥–æ –º–∏–Ω–∏–º—É–º–∞, –º–µ—Ç–æ–¥ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç
     * -1, –≤ –∫–∞—á–µ—Å—Ç–≤–µ –∫–æ–¥–∞ –æ—à–∏–±–∫–∏.
     * 2. –µ—Å–ª–∏ –∏—Å–∫–æ–º—ã–π —ç–ª–µ–º–µ–Ω—Ç –Ω–µ –Ω–∞–π–¥–µ–Ω, –º–µ—Ç–æ–¥ –≤–µ—Ä–Ω–µ—Ç -2 –≤ –∫–∞—á–µ—Å—Ç–≤–µ –∫–æ–¥–∞ –æ—à–∏–±–∫–∏.
     * 3. –µ—Å–ª–∏ –≤–º–µ—Å—Ç–æ –º–∞—Å—Å–∏–≤–∞ –ø—Ä–∏—à–µ–ª null, –º–µ—Ç–æ–¥ –≤–µ—Ä–Ω–µ—Ç -3
     * 4. –ø—Ä–∏–¥—É–º–∞–π—Ç–µ —Å–≤–æ–∏ –≤–∞—Ä–∏–∞–Ω—Ç—ã –∏—Å–∫–ª—é—á–∏—Ç–µ–ª—å–Ω—ã—Ö —Å–∏—Ç—É–∞—Ü–∏–π –∏ –≤–µ—Ä–Ω–∏—Ç–µ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–∏–µ
     * –∫–æ–¥—ã –æ—à–∏–±–æ–∫.
     * 
     * –ù–∞–ø–∏—à–∏—Ç–µ –º–µ—Ç–æ–¥, –≤ –∫–æ—Ç–æ—Ä–æ–º —Ä–µ–∞–ª–∏–∑—É–π—Ç–µ –≤–∑–∞–∏–º–æ–¥–µ–π—Å—Ç–≤–∏–µ —Å –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–º. –¢–æ –µ—Å—Ç—å,
     * —ç—Ç–æ—Ç –º–µ—Ç–æ–¥ –∑–∞–ø—Ä–æ—Å–∏—Ç –∏—Å–∫–æ–º–æ–µ —á–∏—Å–ª–æ —É –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è, –≤—ã–∑–æ–≤–µ—Ç –ø–µ—Ä–≤—ã–π, –æ–±—Ä–∞–±–æ—Ç–∞–µ—Ç
     * –≤–æ–∑–≤—Ä–∞—â–µ–Ω–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ –∏ –ø–æ–∫–∞–∂–µ—Ç —á–∏—Ç–∞–µ–º—ã–π —Ä–µ–∑—É–ª—å—Ç–∞—Ç –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é. –ù–∞–ø—Ä–∏–º–µ—Ä,
     * –µ—Å–ª–∏ –≤–µ—Ä–Ω—É–ª—Å—è -2, –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é –≤—ã–≤–µ–¥–µ—Ç—Å—è —Å–æ–æ–±—â–µ–Ω–∏–µ: ‚Äú–ò—Å–∫–æ–º—ã–π —ç–ª–µ–º–µ–Ω—Ç –Ω–µ
     * –Ω–∞–π–¥–µ–Ω‚Äù.
     */
    static int getIndex(int array[], int value) {
        int result = -1;
        if (array == null)
            return -3;
        if (value < 0)
            return -4;
        if (getArr(array) != -1) {
            for (int i = 0; i < array.length; i++) {
                if (array[i] == value) {
                    result = i;
                    break;
                }
            }
            if (result < 0)
                result = -2;
        }
        return result;
    }

    /**
     * –†–µ–∞–ª–∏–∑—É–π—Ç–µ –º–µ—Ç–æ–¥, –ø—Ä–∏–Ω–∏–º–∞—é—â–∏–π –≤ –∫–∞—á–µ—Å—Ç–≤–µ –∞—Ä–≥—É–º–µ–Ω—Ç–∞ —Ü–µ–ª–æ—á–∏—Å–ª–µ–Ω–Ω—ã–π –¥–≤—É–º–µ—Ä–Ω—ã–π
     * –º–∞—Å—Å–∏–≤. –ù–µ–æ–±—Ö–æ–¥–∏–º–æ –ø–æ—Å—á–∏—Ç–∞—Ç—å –∏ –≤–µ—Ä–Ω—É—Ç—å —Å—É–º–º—É —ç–ª–µ–º–µ–Ω—Ç–æ–≤ —ç—Ç–æ–≥–æ –º–∞—Å—Å–∏–≤–∞. –ü—Ä–∏
     * —ç—Ç–æ–º –Ω–∞–∫–ª–∞–¥—ã–≤–∞–µ–º –Ω–∞ –º–µ—Ç–æ–¥ 2 –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∏—è: –º–µ—Ç–æ–¥ –º–æ–∂–µ—Ç —Ä–∞–±–æ—Ç–∞—Ç—å —Ç–æ–ª—å–∫–æ —Å
     * –∫–≤–∞–¥—Ä–∞—Ç–Ω—ã–º–∏ –º–∞—Å—Å–∏–≤–∞–º–∏ (–∫–æ–ª-–≤–æ —Å—Ç—Ä–æ–∫ = –∫–æ–ª-–≤—É —Å—Ç–æ–ª–±—Ü–æ–≤), –∏ –≤ –∫–∞–∂–¥–æ–π —è—á–µ–π–∫–µ
     * –º–æ–∂–µ—Ç –ª–µ–∂–∞—Ç—å —Ç–æ–ª—å–∫–æ –∑–Ω–∞—á–µ–Ω–∏–µ 0 –∏–ª–∏ 1. –ï—Å–ª–∏ –Ω–∞—Ä—É—à–∞–µ—Ç—Å—è –æ–¥–Ω–æ –∏–∑ —É—Å–ª–æ–≤–∏–π, –º–µ—Ç–æ–¥
     * –¥–æ–ª–∂–µ–Ω –±—Ä–æ—Å–∏—Ç—å RuntimeException —Å —Å–æ–æ–±—â–µ–Ω–∏–µ–º –æ–± –æ—à–∏–±–∫–µ.
     */
    static int getSum(int[][] array) {
        int sum = 0;
        if (array == null)
            throw new RuntimeException("–ú–∞—Å—Å–∏–≤ —Ä–∞–≤–µ–Ω null");
        if (array.length == 0)
            throw new RuntimeException("–ú–∞—Å—Å–∏–≤ –ø—É—Å—Ç");
        if (array[0].length != array.length)
            throw new RuntimeException("–ú–∞—Å—Å–∏–≤ –Ω–µ –∫–≤–∞–¥—Ä–∞—Ç–Ω—ã–π");
        for (int i = 0; i < array.length; i++) {
            for (int j = 0; j < array[0].length; j++) {
                if (array[i][j] != 0 && array[i][j] != 1)
                    throw new RuntimeException("–Ø—á–µ–π–∫–∞ –º–∞—Å—Å–∏–≤–∞ –º–æ–∂–µ—Ç –±—ã—Ç—å —Ç–æ–ª—å–∫–æ 0 –∏–ª–∏ 1");
                sum += array[i][j];
            }
        }
        return sum;
    }

    /**
     * –†–µ–∞–ª–∏–∑—É–π—Ç–µ –º–µ—Ç–æ–¥, –ø—Ä–∏–Ω–∏–º–∞—é—â–∏–π –≤ –∫–∞—á–µ—Å—Ç–≤–µ –∞—Ä–≥—É–º–µ–Ω—Ç–∞ —Ü–µ–ª–æ—á–∏—Å–ª–µ–Ω–Ω—ã–π –¥–≤—É–º–µ—Ä–Ω—ã–π
     * –º–∞—Å—Å–∏–≤. –ù–µ–æ–±—Ö–æ–¥–∏–º–æ –ø–æ—Å—á–∏—Ç–∞—Ç—å –∏ –≤–µ—Ä–Ω—É—Ç—å —Å—É–º–º—É —ç–ª–µ–º–µ–Ω—Ç–æ–≤ —ç—Ç–æ–≥–æ –º–∞—Å—Å–∏–≤–∞. –ü—Ä–∏
     * —ç—Ç–æ–º –Ω–∞–∫–ª–∞–¥—ã–≤–∞–µ–º –Ω–∞ –º–µ—Ç–æ–¥ 2 –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∏—è: –º–µ—Ç–æ–¥ –º–æ–∂–µ—Ç —Ä–∞–±–æ—Ç–∞—Ç—å —Ç–æ–ª—å–∫–æ —Å
     * –∫–≤–∞–¥—Ä–∞—Ç–Ω—ã–º–∏ –º–∞—Å—Å–∏–≤–∞–º–∏ (–∫–æ–ª-–≤–æ —Å—Ç—Ä–æ–∫ = –∫–æ–ª-–≤—É —Å—Ç–æ–ª–±—Ü–æ–≤), –∏ –≤ –∫–∞–∂–¥–æ–π —è—á–µ–π–∫–µ
     * –º–æ–∂–µ—Ç –ª–µ–∂–∞—Ç—å —Ç–æ–ª—å–∫–æ –∑–Ω–∞—á–µ–Ω–∏–µ 0 –∏–ª–∏ 1. –ï—Å–ª–∏ –Ω–∞—Ä—É—à–∞–µ—Ç—Å—è –æ–¥–Ω–æ –∏–∑ —É—Å–ª–æ–≤–∏–π, –º–µ—Ç–æ–¥
     * –¥–æ–ª–∂–µ–Ω –≤–µ—Ä–Ω—É—Ç—å –∫–æ–¥ –æ—à–∏–±–∫–∏. –ü—Ä–æ–≥—Ä–∞–º–º–∞ –¥–æ–ª–∂–Ω–∞ –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ –æ–±—Ä–∞–±–æ—Ç–∞—Ç—å –∫–æ–¥ –æ—à–∏–±–∫–∏ –∏
     * –≤—ã–≤–µ—Å—Ç–∏ —Å–æ–æ—Ç–≤–µ—Ç—Å—Ç–≤—É—é—â–µ–µ —Å–æ–æ–±—â–µ–Ω–∏–µ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é. –°—Ä–∞–≤–Ω–∏—Ç—å —Ç–∞–∫–æ–π –≤–∞—Ä–∏–∞–Ω—Ç
     * –æ–±—Ä–∞–±–æ—Ç–∫–∏ –∏—Å–∫–ª—é—á–µ–Ω–∏–π —Å –ø—Ä–µ–¥—ã–¥—É—â–∏–º. –ö–∞–∫–æ–µ –ø—Ä–µ–∏–º—É—â–µ—Å—Ç–≤–æ —É –∏—Å–∫–ª—é—á–µ–Ω–∏–π –ø–µ—Ä–µ–¥
     * –∫–æ–¥–∞–º–∏ –æ—à–∏–±–æ–∫ –≤—ã –º–æ–∂–µ—Ç–µ –Ω–∞–∑–≤–∞—Ç—å –≤ –¥–∞–Ω–Ω–æ–º —Å–ª—É—á–∞–µ?
     */
    static int getSum2(int[][] array) {
        int sum = 0;
        if (array == null)
            return -1;
        if (array.length == 0)
            return -2;
        if (array[0].length != array.length)
            return -3;
        for (int i = 0; i < array.length; i++) {
            for (int j = 0; j < array[0].length; j++) {
                if (array[i][j] != 0 && array[i][j] != 1)
                    return -4;
                sum += array[i][j];
            }
        }
        return sum;
    }
